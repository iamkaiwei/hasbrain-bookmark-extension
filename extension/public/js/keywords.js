const extract_tags = data => {
  const start_parse_time = new Date();
  const sentences = extract_sentences(data);
  const raw_words = extract_words(sentences);
  const tags_obj = raw_words.reduce(
    (rs, val) => {
      let exist = false;
      categories.forEach(category => {
        if (category.set.has(val)) {
          exist = true;
          if (!rs.jobs[category.name]) rs.jobs[category.name] = 0;
          rs.jobs[category.name] += 1;
        }
      });
      if (exist) {
        val = val.toLowerCase();
        if (!rs.tags[val]) rs.tags[val] = 0;
        rs.tags[val] += 1;
      }
      return rs;
    },
    { tags: {}, jobs: {} }
  );
  const tags = top_freq(tags_obj.tags);
  const jobs = top_freq(tags_obj.jobs, 1);
  const parse_time = new Date() - start_parse_time;
  return { tags, jobs, parse_time };
}

const top_freq = (obj_count, take = 10) => {
  const full = Object.keys(obj_count).map(k => ({
    name: k,
    count: obj_count[k]
  }));
  full.sort((a, b) => {
    return b.count - a.count;
  });
  return full.slice(0, take).map(t => t.name);
}

const extract_sentences = data => {
  return data.split(/[\.!\?]+\s+/g);
}

const extract_words = (sentences, bound = 5) => {
  let rs = [];
  sentences.forEach(sentence => {
    const words = sentence.split(/[^\w\.\+#]+/) || [];
    for (let i = 0; i < words.length; i++) {
      rs.push(words[i]);
      let tmp = words[i];
      for (let j = i + 1; j <= i + bound && j < words.length; j++) {
        tmp += ' ' + words[j];
        rs.push(tmp);
      }
    }
  });
  return rs;
}

const extractWords = (data) => {
  const match = data.match(/body[^]*>[\s\S]*\/body/gi);
  if (!match) return data;

  const body = match[0];
  const content = body.replace(/script\b[^<]*(?:(?!<\/script)[^<]*)*<\/script/gi, '').replace(/style\b[^<]*(?:(?!<\/style)[^<]*)*<\/style/gi, '');
  return content;
}
